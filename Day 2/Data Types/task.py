# Subscripting
print("Hello"[2])
print("Hello"[-1])

# String
print("123" + "Hello")
print(type("hooo"))

# Integer (Whole Numbers)
print(102 + 8)
print(123_234_85) #underscore is ignored
print(type(427))

# Float (Decimal)
print(342.53)
print(type(234.452))

# Boolean (True or False)
print(True)
print(False)
print(type(True))
